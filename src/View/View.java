    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        // TODO add your handling code here:
        String maKhachHang=jComboBox1.getSelectedItem().toString();
        String maBan=jComboBox1.getSelectedItem().toString();
        Date date=jDateChooser1.getDate();
        String ngayThang=DateFormat.getDateInstance().format(date);
        KhachHang std = null;
        Nhahang std1 = null;
        try{
            for(KhachHang i:listKhachhang){
                if(i.getMa().equals(maKhachHang)){
                    std=i;

                }
            }
            for(Nhahang j:listNhaHang){
                if(j.getMaBan().equals(maBan)){
                    std1=j;
                }
            }
            Quanlidatban datban=new Quanlidatban(std, std1, ngayThang);
            quanlidatban.add(datban);
            tableModelDatBan.addRow(datban.toObject());
            IOFile.viet(filequanlidatban, quanlidatban);
        }
        catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, ex.toString());
        }
        
        
        
    }//GEN-LAST:event_jButton9ActionPerformed

